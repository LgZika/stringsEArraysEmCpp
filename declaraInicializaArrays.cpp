#include <iostream>

using namespace std;

int main() { 
    // Declarando um array de inteiros com 5 elementos 
    int array [5] = {10, 20, 30, 40, 50};

    // Acessando e imprimindo os elementos do array
    cout << "Elemento 1: " << array[0] << endl;   // Primeiro elemento (indice 0) 
    cout << "Elemento 2: " << array[1] << endl;   // Segundo elemento (indice 1)
    cout << "Elemento 3: " << array[2] << endl;   // Terceiro elemento (indice 2)
    cout << "Elemento 4: " << array[3] << endl;   // Quarto elemento (indice 3)
    cout << "Elemento 5: " << array[4] << endl;   // Quinto elemento (indice 4)
    return 0;
    }